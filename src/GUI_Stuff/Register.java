    private void ConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmActionPerformed
     // Declaring variables such as first name last name etc. 
     int id = 0;
     String fi = First_Name.getText().trim();
     String la = Last_Name.getText().trim();
     String us = New_usn.getText().trim();
     String pass = New_pswd.getText().trim();
   
     try{
       // Parsing Admin_ID gettext so that any thing read in the Admin_ID text field is read as an integer. 
     id = Integer.parseInt(Admin_ID.getText());
           
    }
     // Catching NumberFormatException while displayign JOptionPane message which displays an error message stating that the User must enter an integer. 
    catch(NumberFormatException ex){
          JOptionPane.showMessageDialog(null,"Please Enter an Integer");
           
        }
        try {
            // Establishing connection and creating statement
            Connection conn = DriverManager.getConnection("jdbc:derby:CourseDB_Ebd;create=true", "pdc", "pdc");
            Statement st = conn.createStatement();
        // Checking if text fields are empty if so print out error message
            if( Admin_ID.getText().equals("")|| fi.equals("") || la.equals("") || us.equals("") || pass.equals("")){
                
                JOptionPane.showMessageDialog(null,"Not all fields have been submitted");
            }
            // if the firstname textfield and lastname textfield deos not match the 
            else if(!fi.matches("[a-zA-z]+") || !la.matches("[a-zA-z]+")){
             JOptionPane.showMessageDialog(null,"Invalid first name (Please Enter a String)");
              this.setVisible(false);
              new Register().setVisible(true);
         }
            // Checks if id is not alphabetic and if id is not 0. 
            else if(!Character.isAlphabetic(id) && id != 0){
              st.execute("INSERT INTO ADMIN (ADMIN_ID, FIRST_NAME, LAST_NAME, USERNAME, PASSWORD)VALUES ("+id+",'"+fi+"','"+la+"','"+us+"','"+pass+"')");
              JOptionPane.showMessageDialog(null, "Admin Registered."); 
              this.setVisible(false);
              new Logging_In().setVisible(true);
            }
            
           // Catching NumberFormatException
        } catch(NumberFormatException ex){
          JOptionPane.showMessageDialog(null,"Not all fields have been submitted");
        }
        // Catches SQLIntegrityConstraintViolation
       catch (SQLIntegrityConstraintViolationException eg) {
                          JOptionPane.showMessageDialog(null,"ID and/or Username exists in database");
}
        // Catches SQLException
        catch (SQLException ex) {
            Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
        }  
    }//GEN-LAST:event_ConfirmActionPerformed

